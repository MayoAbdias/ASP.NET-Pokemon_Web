        public List<Pokemon> filtrar(string campo, string criterio, string filtro, string estado)
        {
            List<Pokemon> lista = new List<Pokemon>();
            AccesoDatos datos = new AccesoDatos();
            try
            {
                string consulta = "Select Numero, Nombre, P.Descripcion, UrlImagen, E.Descripcion Tipo, D.Descripcion Debilidad, P.IdTipo, P.IdDebilidad, P.Id, P.Activo From ELEMENTOS E, POKEMONS P, ELEMENTOS D where E.id = P.IdTipo AND D.id = P.IdDebilidad AND ";
                if (campo == "Número")
                {
                    switch (criterio)
                    {
                        case "Mayor a":
                            consulta += "Numero > " + filtro;
                            break;
                        case "Menor a":
                            consulta += "Numero < " + filtro;
                            break;

                        default:
                            consulta += "Numero =" + filtro;
                            break;
                    }
                }//CORREIR ACAA
                else if (campo == "Tipo")
                {
                    switch (criterio)
                    {
                        case "Empieza con":
                            consulta += "Tipo like '" + filtro + "%'";
                            break;
                        case "Termina con":
                            consulta += "Tipo like '%" + filtro + "'";
                            break;

                        default:
                            consulta += "Tipo like '%" + filtro + "%'" ;
                            break;
                    }
                }
                else
                {
                    switch (criterio)
                    {
                        case "Empieza con":
                            consulta += "Nombre like '" + filtro + "%'";
                            break;
                        case "Termina con": 
                            consulta += "Nombre like '%" +  filtro + "'";
                            break;

                        default:
                            consulta += "Nombre like '%" + filtro + "%'";
                            break;
                    }
                }

                if (estado == "Activo")
                    consulta += "and P.Activo = 1";
                else if (estado == "Inactivo")
                    consulta += "and P.Activo = 0";

                datos.setearConsulta(consulta);
                datos.ejecutarLectura();
                while (datos.Lector.Read())
                {
                    Pokemon aux = new Pokemon();
                    aux.Id = (int)datos.Lector["Id"];
                    aux.Numero = (int)datos.Lector["Numero"];
                    aux.Nombre = (string)datos.Lector["Nombre"];
                    aux.Descripcion = (string)datos.Lector["Descripcion"];
